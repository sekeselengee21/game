CREATE INDEX idx_poker_game_session_table_date ON poker_game_session(table_id, create_date DESC);
CREATE INDEX idx_poker_hand_history_table_date ON poker_hand_history(table_id, created_at DESC);
CREATE INDEX idx_poker_chat_message_table_date ON poker_chat_message(table_id, created_at DESC);
CREATE INDEX idx_poker_deposit_user_date ON poker_deposit(user_id, create_date DESC);
CREATE INDEX idx_poker_withdrawal_user_date ON poker_withdrawal(user_id, create_date DESC);
CREATE INDEX idx_poker_outcome_user_accounted ON poker_outcome(user_id, is_accounted);
