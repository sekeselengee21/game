package dev.manestack.service.user;

public class DataBlock {
    private String name;
    private String value;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String content) {
        this.value = content;
    }
}
